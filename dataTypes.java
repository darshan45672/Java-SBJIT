// Data Types in Java

class DataTypes{
    
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;

        byte byteVar1 = 100;
        byte byteVar2 = 010;

        short shortVar1 = 10000;
        short shortVar2 = 02000;

        long longVar1 = 100000L;
        long longVar2 = 020000L;

        float floatVar1 = 10.5f;
        float floatVar2 = 20.0f;

        double doubleVar1 = 100.99;
        double doubleVar2 = 200.99;

        System.out.println("Integer Addition: " + (num1 + num2));
        System.out.println("Byte Addition: " + (byteVar1 + byteVar2));
        System.out.println("Short Addition: " + (shortVar1 + shortVar2));
        System.out.println("Long Addition: " + (longVar1 + longVar2));
        System.out.println("Float Addition: " + (floatVar1 + floatVar2));
        System.out.println("Double Addition: " + (doubleVar1 + doubleVar2));


    }
}